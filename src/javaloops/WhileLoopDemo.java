package javaloops;

public class WhileLoopDemo {
    public static void main(String[] args) {

        /*
        initialization, condition, increment/decrement by 1
        1. int = data type.
        2  i<0; initialization,
        4. while = keyword
        5. (i<5) = condition is true
        6. sout("") code/task
        7. i++ decrement/decrement by 1

        Note : Check condition first is true then print
         */

        int i=6;
        while (i<5){
            System.out.println("Covid-19");
            i++;
        }
    }
}
